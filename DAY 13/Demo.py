import numpy as np
#creating a numpy array
arr=np.array([[1,2,3],[2,3,4]])
#printing the type of array
print(type(arr))
#printing the number of dimensions
print(arr.ndim)
#printing the shape of array
print(arr.shape)
#printing the size of array
print(arr.size)
#printing the datatype of elements
print(arr.dtype)